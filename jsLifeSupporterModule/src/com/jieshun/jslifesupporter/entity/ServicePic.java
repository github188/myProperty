package com.jieshun.jslifesupporter.entity;

public class ServicePic {

	private int servicePicId;
	//图片云平台保存地址
	private String serverSavedUrl;
	//图片手机本地地址
	private String picLocalUrl;
	//是否上传unupload、successed、failed
	private String isUpload;
	//图片大小，按字节计算
	private String fileSize;
	//图片上传时间
	private String uploadTime;
	//图片索引。现在无用
	private int picIndex;
	public int getServicePicId() {
		return servicePicId;
	}
	public void setServicePicId(int servicePicId) {
		this.servicePicId = servicePicId;
	}
	public String getServerSavedUrl() {
		return serverSavedUrl;
	}
	public void setServerSavedUrl(String serverSavedUrl) {
		this.serverSavedUrl = serverSavedUrl;
	}
	public String getPicLocalUrl() {
		return picLocalUrl;
	}
	public void setPicLocalUrl(String picLocalUrl) {
		this.picLocalUrl = picLocalUrl;
	}
	public String getIsUpload() {
		return isUpload;
	}
	public void setIsUpload(String isUpload) {
		this.isUpload = isUpload;
	}
	
	
	public String getFileSize() {
		return fileSize;
	}
	public void setFileSize(String fileSize) {
		this.fileSize = fileSize;
	}
	public String getUploadTime() {
		return uploadTime;
	}
	public void setUploadTime(String uploadTime) {
		this.uploadTime = uploadTime;
	}
	public int getPicIndex() {
		return picIndex;
	}
	public void setPicIndex(int picIndex) {
		this.picIndex = picIndex;
	}
	
	
	
}
